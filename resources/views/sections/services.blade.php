                    <h1>COC</h1>
                    <p class="text-muted">What is a Continuum of Care (CoC)?</p>
                    <hr>
                    <h4>The Continuum of Care is a regional planning body of representative stakeholders designed to
                        promote a shared commitment to the goal of ending homelessness with a focus on;</h4>

                    <ul class="list-group text-left" style="margin-left:10%">
                        <li>Quickly rehousing homeless individuals and families while
                            minimizing the trauma and dislocation caused to homeless individuals, families, and communities by homelessness;
                        </li>
                        <li>Promoting access to and effect utilization of mainstream
                            programs; and</li>
                        <li>Optimizing stability and self-sufficiency for individuals and
                            families while homeless and once housed.
                        </li>
                    </ul>

                    <h3>It includes</h3>

                    <ul class="list-group text-left" style="margin-left:10%">
                        <li>Gathering and analyzing information to determine the local needs of people experiencing homelessness;</li>
                        <li>Implementing strategic plans to end homelessness based on data;</li>
                        <li>Measuring results of regional plan; and</li>
                        <li>Setting a local process for applying, reviewing and prioritizing HUD CoC project applications.</li>
                    </ul>

